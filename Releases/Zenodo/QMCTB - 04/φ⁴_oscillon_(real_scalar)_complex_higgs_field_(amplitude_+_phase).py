# ---- Initial complex field: localized amplitude + small random phase ----
amp0 = 0.7 * v
width = 0.7
rho0 = amp0 * np.exp(-(R/width)**2)
theta0 = 0.15 * np.random.randn(*R.shape)
phi1 = (rho0 * np.cos(theta0)).astype(np.float32)
phi2 = (rho0 * np.sin(theta0)).astype(np.float32)
prev1 = phi1.copy()
prev2 = phi2.copy()

# ---- Laplacian kernel (7-point in 3D) ----
kernel = np.zeros((3,3,3), dtype=np.float32)
kernel[0,1,1]=kernel[2,1,1]=kernel[1,0,1]=kernel[1,2,1]=kernel[1,1,0]=kernel[1,1,2]=1
kernel[1,1,1] = -6

# ---- PML damping near boundaries ----
pml = 10
damp = np.ones((gx,gy,gz), dtype=np.float32)
for i in range(pml):
    f = ((i+1)/pml)**2
    damp[i,:,:] *= f; damp[-1-i,:,:] *= f
    damp[:,i,:] *= f; damp[:,-1-i,:] *= f
    damp[:,:,i] *= f; damp[:,:,-1-i] *= f

# ---- force for Higgs (component-wise) ----
def higgs_force(p1, p2):
    r2 = p1*p1 + p2*p2
    fac = lambda_phi * (r2 - v*v)
    return fac * p1, fac * p2

# ---- storage ----
frames = []
energies = []

print("Running complex Higgs evolution:", n_steps, "steps; grid", gx,gy,gz)
tstart = time.time()
midz = gz//2

for step in range(n_steps):
    lap1 = convolve(phi1, kernel, mode='mirror') / (dx*dx)
    lap2 = convolve(phi2, kernel, mode='mirror') / (dx*dx)
    f1, f2 = higgs_force(phi1, phi2)

    new1 = 2*phi1 - prev1 + (c*dt)**2 * (lap1 - f1)
    new2 = 2*phi2 - prev2 + (c*dt)**2 * (lap2 - f2)

    # damping
    new1 *= damp
    new2 *= damp

    prev1, phi1 = phi1, new1
    prev2, phi2 = phi2, new2

