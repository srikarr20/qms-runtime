import pandas as pd
from tqdm import tqdm

# -------- Configuration --------
video_path = "/content/drive/My Drive/ImprintField_phi4_visuals/phi4_mode2C_cinematic.mp4"
v = 1.0
fps_override = None  # set to float to override fps from file, else None
resize_max_dim = 512  # set None to keep original resolution; reduces FFT cost if big
use_roi_centroid = True  # if True, estimate core centroid each frame for center_phi
out_csv = "/content/drive/My Drive/ImprintField_phi4_visuals/phi_diagnostics_summary.csv"
out_plot = "/content/drive/My Drive/ImprintField_phi4_visuals/phi_diagnostics_plot.png"
save_plots = True

# -------- Sanity checks --------
if not os.path.exists(video_path):
    raise FileNotFoundError(f"Video not found: {video_path}")

cap = cv2.VideoCapture(video_path)
if not cap.isOpened():
    raise RuntimeError("Failed to open video. Check file/permissions.")

# Prefer reported frame count, but fall back to reading frames loop if unavailable
frame_count = int(cap.get(cv2.CAP_PROP_FRAME_COUNT)) or None
fps = fps_override or (cap.get(cv2.CAP_PROP_FPS) or 25.0)

# If frame_count is available, pre-allocate a list capacity hint (Python list cannot pre-size)
frames = []

print(f"Opening video: {video_path}")
print(f"Reported FPS: {fps:.2f}, Reported frame count: {frame_count}")

# --- Load frames (grayscale); optionally resize to speed processing ---
read_iter = range(frame_count) if frame_count else iter(int, 1)
pbar = tqdm(total=frame_count if frame_count else None, desc="Reading frames")
while True:
    ret, frame = cap.read()
    if not ret:
        break
    gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
    if resize_max_dim is not None:
        h, w = gray.shape
        max_dim = max(h, w)
        if max_dim > resize_max_dim:
            scale = resize_max_dim / max_dim
            new_w, new_h = int(w * scale), int(h * scale)
            gray = cv2.resize(gray, (new_w, new_h), interpolation=cv2.INTER_AREA)
    frames.append(gray.astype(np.float32))
    pbar.update(1)
pbar.close()
cap.release()

if len(frames) == 0:
    raise RuntimeError("No frames were read from the video.")

frames = np.stack(frames)  # shape (T, H, W)
T, H, W = frames.shape
print(f"🎥 Loaded video: {T} frames, resolution {H}x{W}")

# --- Brightness → φ mapping (0→−v, 255→+v) ---
phi_frames = (frames / 255.0) * 2.0 * v - v

# Preallocate metric arrays
center_phi = np.zeros(T, dtype=np.float64)
spatial_var = np.zeros(T, dtype=np.float64)
core_frac = np.zeros(T, dtype=np.float64)
energy_proxy = np.zeros(T, dtype=np.float64)
highk_power = np.zeros(T, dtype=np.float64)

# Precompute k-radius grid for FFT masking (uses shifted indices)
ny, nx = H, W
yy, xx = np.mgrid[-ny//2:(ny//2 + ny%2), -nx//2:(nx//2 + nx%2)]
kr_grid = np.sqrt(xx**2 + yy**2)
# threshold in pixel-frequency units (tuneable)
highk_thresh = 0.25 * max(nx, ny)

# Optional ROI centroid initial guess (center of frame)
cy0, cx0 = H // 2, W // 2
roi_radius = int(min(H, W) * 0.25)  # region to compute centroid within (25% of smaller dim)

for t in range(T):
    phi = phi_frames[t]
    spatial_var[t] = np.var(phi)

    # Core fraction: fraction of pixels with |phi| > threshold
    core_frac[t] = np.mean(np.abs(phi) > 0.6 * v)

    # Center phi — either frame center or centroid of high-|phi| region (more robust)
    if use_roi_centroid:
        # mask around previous center to find core; thresholding helps ignore background
        y0, x0 = cy0, cx0
        y1, y2 = max(0, y0 - roi_radius), min(H, y0 + roi_radius)
        x1, x2 = max(0, x0 - roi_radius), min(W, x0 + roi_radius)
        sub = phi[y1:y2, x1:x2]
        # find centroid of strong-field pixels (abs phi above 0.6v)
        mask = np.abs(sub) > 0.6 * v
        if mask.sum() > 5:
            coords = np.argwhere(mask)
            mean_rel = coords.mean(axis=0)  # (ry, rx)
            cy0 = int(y1 + mean_rel[0])
            cx0 = int(x1 + mean_rel[1])
        # fallback to center if centroid unstable
    cy, cx = cy0, cx0
    # Clip indices
    cy = np.clip(cy, 0, H - 1)
    cx = np.clip(cx, 0, W - 1)
    center_phi[t] = phi[cy, cx]

    # Energy proxy: gradient energy + potential
    gy, gx = np.gradient(phi)  # gy: d/dy, gx: d/dx
    grad2 = 0.5 * (gx**2 + gy**2)
    pot = 0.25 * (phi**2 - v**2)**2
    energy_proxy[t] = np.sum(grad2 + pot)

    # Spectrum & high-k power fraction (use shifted FFT so kr_grid aligns)
    F = np.fft.fftshift(np.fft.fft2(phi))
    P = np.abs(F) ** 2
    total_power = np.sum(P) + 1e-12
    highk_power[t] = np.sum(P[kr_grid > highk_thresh]) / total_power

# --- Smooth for clarity ---
center_s = gaussian_filter1d(center_phi, sigma=2)
var_s = gaussian_filter1d(spatial_var, sigma=2)
core_s = gaussian_filter1d(core_frac, sigma=2)
E_s = gaussian_filter1d(energy_proxy, sigma=2)
hk_s = gaussian_filter1d(highk_power, sigma=2)
